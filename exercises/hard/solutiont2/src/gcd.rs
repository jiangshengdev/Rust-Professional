/// 计算两个数字的最大公约数
///
/// # 参数
/// a - 第一个数字，类型为 u128
/// b - 第二个数字，类型为 u128
///
/// # 返回值
/// 返回 a 和 b 的最大公约数
pub fn gcd(mut a: u128, mut b: u128) -> u128 {
    // 当 b 不为 0 时不断计算余数
    while b != 0 {
        // 保存 a 对 b 取余的结果
        let temp = a % b;
        // 将 a 更新为 b
        a = b;
        // 将 b 更新为余数
        b = temp;
    }
    // b 为 0 时，a 即为最大公约数
    a
}
